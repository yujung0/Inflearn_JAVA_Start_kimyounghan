package operator.ex;

public class OperationEx1 {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;
        int num3 = 30;

        int sum = num1 + num2 + num3; // 합
        int average = sum/3; // 평균

        System.out.println("합 : " + sum);
        System.out.println("평균 : " + average);
    }
}
